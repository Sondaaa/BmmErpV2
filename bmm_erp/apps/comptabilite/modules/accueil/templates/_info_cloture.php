                <h3 class="smaller lighter blue no-margin">Clôture comptable : Définition de la clôture de l'exercice</h3>
            </div>

            <div class="modal-body">
                <div class="row">
                    <div class="col-xs-12" style="overflow-y: auto; height: 400px; line-height: 25px;">
                        <legend class="blue">Clôture comptable</legend>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            Dans le vocabulaire de la comptabilité, la clôture correspond à l'achèvement des 
                            enregistrements comptable, le rassemblement des pièces justificatives ainsi que 
                            l'ensemble des opérations qui permettent d'éditer le bilan de la société.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            C'est une procédure comptable et une fonctionnalité de notre <u>Système d'Informations</u> 
                        pour figer les comptes et passer à l'année suivante.
                        </p>
                        <legend class="green">Quand réaliser la clôture comptable de l'entreprise ?</legend>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            À l'exception des professions libérales, les entreprises ont le libre choix de la 
                            date de clôture de leur exercice comptable. À la création de la société, les 
                            dirigeants choisissent les dates de début et de fin d'exercice. Chaque année 
                            fiscale doit comporter 12 mois. Des exceptions existent pour les entreprises en 
                            démarrage qui peuvent avoir un exercice plus ou moins long.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            La date choisie pour la clôture de l'exercice doit être la même tous les ans. Cette 
                            souplesse permet aux sociétés qui exercent une activité soumise à un effet de 
                            saisonnalité de choisir des périodes plus pertinentes que l'année civile. Le choix 
                            de ne pas opter pour un exercice du 1er janvier au 31 décembre est souvent motivé 
                            par des raisons pratiques.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            La clôture comptable concerne toutes les entreprises qui ont une obligation de dépôt 
                            de bilans à l'administration. Même si les micro-entrepreneurs (ex-auto-entrepreneurs) 
                            ne sont pas soumis à cette procédure, il demeure pertinent de réaliser des bilans 
                            financiers pour analyser l'activité dans son ensemble.
                        </p>
                        <legend class="orange">Réaliser le bilan et éditer la liasse fiscale</legend>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            La clôture des écritures comptables demande la réalisation des inventaires, ainsi 
                            que de comptabiliser l'ensemble des opérations financières qui ont impacté 
                            l'entreprise durant l'année qui se termine.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            Grâce à ces informations enregistrées dans les livres de comptabilité de la société, 
                            il est possible d'établir le bilan et le compte de résultat. Une liasse fiscale est 
                            éditée, mentionnant le montant des bénéfices nets réalisés par l'entreprise. C'est 
                            sur cette base que le calcul des impôts est réalisé, le montant variant en fonction 
                            du régime fiscal adopté.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            L'édition de ces documents est une phase importante. Il s'agit très concrètement d'une 
                            photographie des finances de l'entreprise. L'ensemble de ces éléments sont présentés 
                            en assemblée générale pour approbation, avant de réaliser le dépôt obligatoire auprès 
                            du greffe du tribunal de commerce.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            Une fois la clôture réalisée dans le logiciel de comptabilité, l'ensemble des écritures 
                            et des livres sont figés. Ils doivent alors être archivés avec les pièces justificatives 
                            afférentes.
                        </p>
                        <legend class="brown">Analyser la santé financière de l'entreprise à l'issue de la clôture comptable</legend>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            La clôture comptable de l'entreprise permet d'opérer des comparaisons entre les différents 
                            exercices sociaux. En effet, l'entrepreneur est en mesure d'analyser l'évolution de son 
                            chiffre d'affaires, de ses charges et de leurs impacts sur la santé financière de 
                            l'entreprise. Il s'agit d'un type d'analyse que l'on peut également réaliser au cours de 
                            l'année, grâce à des rapports BI pour, par exemple, comparer le chiffre d'affaires de ce 
                            trimestre par rapport à l'année passée. De cette façon, la stratégie de l'entreprise est 
                            ajustée au cours de l'exercice. La clôture des comptes valide les résultats des choix 
                            stratégiques réalisés. L'entrepreneur dispose alors de clefs pour définir les prochains 
                            objectifs, et pour mettre en œuvre les moyens d'y parvenir : investissement, recrutement, 
                            campagnes publicitaires, changement de méthodologie de gestion de stocks, etc.
                        </p>
                        <p style="text-align: justify; font-size: 14px; width: 94%; margin-left: 3%;">
                            Réalisée à chaque fin d'exercice social, la clôture des comptes permet le calcul des 
                            bénéfices déclarés auprès de l'administration, ainsi que l'édition du bilan et compte de 
                            résultat.
                        </p>
